    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String codigo = txtcodigo.getText();
       String nombre = txtname.getText();
       double precioCosto = Double.parseDouble(txtprecio.getText());
       int cantidadDeVentas = Integer.parseInt(txtventa.getText());
       int existencia = Integer.parseInt(txtexistencia.getText());
       String empresaProductora = TxtEmpresa.getText();
       
       ProductoNacional pn = new ProductoNacional(empresaProductora, codigo, nombre, precioCosto, precioCosto, cantidadDeVentas);
       cadena.agregarProducto(pn);
        JOptionPane.showMessageDialog(rootPane, "Producto agragado");
       // JOptionPane.showMessageDialog(rootPane, "AÃ±ada Los Datos Requeridos");
    }//GEN-LAST:event_jButton1ActionPerformed

